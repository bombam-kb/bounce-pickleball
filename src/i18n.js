export const t = (key, lang, vars) => {
  const entry = DICT[key]
  let s = entry ? (entry[lang] || entry.th) : key
  if (vars) Object.entries(vars).forEach(([k, v]) => { s = s.replace(`{${k}}`, v) })
  return s
}

