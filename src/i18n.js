export const fmtDate = (iso, lang) => {
  const d = new Date(iso + 'T00:00:00')
  return `${DAY_NAMES[lang][d.getDay()]} ${d.getDate()} ${MONTH_NAMES[lang][d.getMonth()]}`
}
